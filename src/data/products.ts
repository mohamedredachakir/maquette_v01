export function getProductsByCollection(collection: Collection): Product[] {
  return products.filter(p => p.collection === collection);
}

export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.featured);
}

export function getNewProducts(): Product[] {
  return products.filter(p => p.new);
}

export function filterProducts(
  ps: Product[],
  filters: {
    paperType?: PaperType[];
    useCase?: UseCase[];
    size?: ProductSize[];
    collection?: Collection[];
  }
): Product[] {
  return ps.filter(p => {
    if (filters.paperType?.length && !filters.paperType.some(t => p.paperType.includes(t))) return false;
    if (filters.useCase?.length && !filters.useCase.some(u => p.useCase.includes(u))) return false;
    if (filters.size?.length && !filters.size.includes(p.size)) return false;
    if (filters.collection?.length && !filters.collection.includes(p.collection)) return false;
    return true;
  });
}
